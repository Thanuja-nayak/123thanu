package Assignments;

public class GradeCalculator {

	public static void main(String[] args) {
		int a=110;
		if(a>=90 && a<=100) {
			System.out.println("A grade");
		}else if(a>=80 && a<=89) {
			System.out.println("B grade");
		}else if(a>=70 && a<=79) {
			System.out.println("C grade");
		}else if(a>=60 && a<=69){
			System.out.println("D grade");
		}else 
			System.out.println("F grade");
		}

}
