package Assignments;

public class GradeCalculator {

	public static void main(String[] args) {
		int a=60;
		if(a>=80) {
			System.out.println("A grade");
		}else if(a>=60) {
			System.out.println("B grade");
		}else if(a>=40) {
			System.out.println("C grade");
		}else {
			System.out.println("D grade");
		}
	}

}
