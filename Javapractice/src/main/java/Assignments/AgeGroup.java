package Assignments;

public class AgeGroup {

	public static void main(String[] args) {
		int age=66;
		if(age>65) {
			System.out.println("Senior citizen");
		}else if(age>18) {
			System.out.println("Adult");
		}else if(age>13) {
			System.out.println("Teenager");
		}else {
			System.out.println("Child");
		}

	}

}
