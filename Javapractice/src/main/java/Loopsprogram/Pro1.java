package Loopsprogram;

public class Pro1 {

	public static void main(String[] args) {
		//Zero one triangle
		for(int i=1;i<=4;i++) {
			if(i%2!=0) {
				for(int j=1;j<=i;j++) {
					System.out.print(j%2);
				}
				System.out.println();
			}else {
				for(int j=1;j<=i;j++) {
					System.out.print((j+1)%2);
				}
				System.out.println();
			}
			
		}	
	        }
	   
	        }