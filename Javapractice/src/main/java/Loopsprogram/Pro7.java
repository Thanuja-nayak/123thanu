package Loopsprogram;
public class Pro7 {

	public static void main(String[] args) {
		// Triangle star pattern
    for(int i = 1;i<=4;i++) {
    	for(int j=i;j<5;j++) {
    	  System.out.print(" ");
	} 
    	for(int k =0;k<i;k++) {
    	  System.out.print("*"+" ");
    	 // System.out.print(" ");
    	} 
    	for(int n=0;n<5;n++) {
    	System.out.print(" ");
    	}
    	System.out.println();
}
}
}