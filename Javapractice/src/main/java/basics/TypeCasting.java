package basics;

public class TypeCasting {

	public static void main(String[] args) {
		int a=10;
		System.out.println(a);
		//float b=(float)a;
		float b=5.5f;
		System.out.println(b);
		byte c=20;
		int e= c;
		System.out.println(e);
		
		int f=400;
		byte g=(byte)f;
		System.out.println(g);
		char h='a';
		int i=h;
		System.out.println(i);
		int j=100;
		char k=(char)j;
		System.out.println(k );
	}

}
