package basics;

public class Switch {

	public static void main(String[] args) {
		int num=1;
		switch(num) {
		case 1:
			System.out.println("kannada");
			break;
		case 2:
			System.out.println("english");
			break;
		case 3:
			System.out.println("hindi");
			break;
		}
	}

}
